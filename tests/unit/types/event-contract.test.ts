describe('CLI-E01 summary event', () => {
  it('parses valid summary event', () => {
    const event = {
      type: 'summary',
      data: { ...SUMMARY_FIXTURE, seq: 8 },
    };
    expect(() => summaryEventSchema.parse(event)).not.toThrow();
  });

  it('requires disclaimer and is_degraded fields', () => {
    const event = {
      type: 'summary',
      data: {
        consensus: [],
        disagreements: [],
        recommendation: 'x',
        confidence: 'medium',
        open_questions: [],
        evidence_refs: [],
        // missing disclaimer and is_degraded
        seq: 1,
      },
    };
    expect(() => summaryEventSchema.parse(event)).toThrow();
  });
});

