/**
 * CLI-E01 — SSE 事件契约一致性验证
 *
 * 验证点：
 * 1. 所有 12 种事件类型可通过 sseEventSchema 运行时解析
 * 2. strict 模式：额外字段会被拒绝
 * 3. 字段名使用 snake_case（无 camelCase 漂移）
 * 4. required 字段缺失时解析失败
 */

import { describe, expect, it } from 'vitest';

import {
  sseEventSchema,
  progressEventSchema,
  chunkEventSchema,
  modelDoneEventSchema,
  modelErrorEventSchema,
  roundDoneEventSchema,
  anonymizeEventSchema,
  roundSummaryEventSchema,
  summaryEventSchema,
  doneEventSchema,
  restoreEventSchema,
  errorEventSchema,
  interruptAckEventSchema,
} from '@/lib/types/schemas';

// ─── Fixtures ─────────────────────────────────────────────────────────────────

const SUMMARY_FIXTURE = {
  consensus: [
    {
      content: 'Deploy with staged rollout.',
      supporting_models: ['m1', 'm2'],
      evidence_refs: ['r1'],
    },
  ],
  disagreements: [],
  recommendation: 'Proceed with staged rollout.',
  confidence: 'medium' as const,
  open_questions: [],
  evidence_refs: ['r1'],
  disclaimer: 'Generated by secretary.',
  is_degraded: false,
};

// ─── Individual schema tests ──────────────────────────────────────────────────

describe('CLI-E01 progress event', () => {
  it('parses valid progress event', () => {
    const event = {
      type: 'progress',
      data: { round: 1, total_rounds: 3, phase: 'independent', seq: 1 },
    };
    expect(() => progressEventSchema.parse(event)).not.toThrow();
  });

  it('rejects extra fields (strict)', () => {
    const event = {
      type: 'progress',
      data: { round: 1, total_rounds: 3, phase: 'independent', seq: 1, extra: 'field' },
    };
    expect(() => progressEventSchema.parse(event)).toThrow();
  });

  it('rejects missing seq', () => {
    const event = { type: 'progress', data: { round: 1, total_rounds: 3, phase: 'independent' } };
    expect(() => progressEventSchema.parse(event)).toThrow();
  });
});

describe('CLI-E01 chunk event', () => {
  it('parses valid chunk event', () => {
    const event = {
      type: 'chunk',
      data: {
        logical_model_id: 'openai/gpt-4',
        actual_model_id: 'openai/gpt-4',
        round: 1,
        content: 'hello',
        done: false,
        seq: 2,
      },
    };
    expect(() => chunkEventSchema.parse(event)).not.toThrow();
  });

  it('requires snake_case field names (not camelCase)', () => {
    // camelCase should fail in strict mode
    const event = {
      type: 'chunk',
      data: {
        logicalModelId: 'openai/gpt-4',  // camelCase — wrong
        actual_model_id: 'openai/gpt-4',
        round: 1,
        content: 'hello',
        done: false,
        seq: 2,
      },
    };
    expect(() => chunkEventSchema.parse(event)).toThrow();
  });
});

