describe('CLI-E01 model_error event', () => {
  it('parses valid model_error event with all required fields', () => {
    const event = {
      type: 'model_error',
      data: {
        logical_model_id: 'm1',
        actual_model_id: null,
        round: 1,
        error_type: 'timeout',
        action: 'skipped',
        degraded_to: null,
        message: 'timed out',
        seq: 4,
      },
    };
    expect(() => modelErrorEventSchema.parse(event)).not.toThrow();
  });

  it('rejects unknown action values', () => {
    const event = {
      type: 'model_error',
      data: {
        logical_model_id: 'm1',
        actual_model_id: null,
        round: 1,
        error_type: 'timeout',
        action: 'unknown_action',  // not allowed
        degraded_to: null,
        message: 'timed out',
        seq: 4,
      },
    };
    expect(() => modelErrorEventSchema.parse(event)).toThrow();
  });
});

