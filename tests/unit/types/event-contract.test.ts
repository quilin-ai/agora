// ─── Full discriminated union ─────────────────────────────────────────────────

describe('CLI-E01 sseEventSchema union covers all 12 types', () => {
  const validEvents = [
    { type: 'progress', data: { round: 1, total_rounds: 3, phase: 'independent', seq: 1 } },
    {
      type: 'chunk',
      data: {
        logical_model_id: 'm1', actual_model_id: 'm1',
        round: 1, content: 'hi', done: false, seq: 2,
      },
    },
    {
      type: 'model_done',
      data: {
        logical_model_id: 'm1', actual_model_id: 'm1',
        round: 1, tokens: { input: 50, output: 100 }, seq: 3,
      },
    },
    {
      type: 'model_error',
      data: {
        logical_model_id: 'm1', actual_model_id: null, round: 1,
        error_type: 'timeout', action: 'skipped', degraded_to: null,
        message: 'timed out', seq: 4,
      },
    },
    {
      type: 'round_done',
      data: {
        round: 1, completed_models: ['m1'], skipped_models: [],
        failed_models: [], total_models: 1, seq: 5,
      },
    },
    {
      type: 'round_summary',
      data: { round: 1, next_round: 2, ...SUMMARY_FIXTURE, seq: 6 },
    },
    { type: 'anonymize', data: { round: 2, labels: ['A', 'B'], seq: 7 } },
    { type: 'summary', data: { ...SUMMARY_FIXTURE, seq: 8 } },
    { type: 'done', data: { total_raw_cost: 0.1, total_platform_price: 0.12, seq: 9 } },
    {
      type: 'restore',
      data: {
        resume_mode: 'state_restore', can_stream: false,
        current_status: 'completed', current_round: 3,
        last_completed_round: 4, completed_round_messages: [],
        summary: null,
      },
    },
    { type: 'error', data: { code: 'ERR', message: 'boom' } },
    { type: 'interrupt_ack', data: { status: 'acknowledged', message: 'ok', seq: 10 } },
  ];

  it.each(validEvents)('parses %s event via discriminated union', (event) => {
    expect(() => sseEventSchema.parse(event)).not.toThrow();
  });

  it('covers exactly 12 event types', () => {
    expect(validEvents).toHaveLength(12);
  });
});
